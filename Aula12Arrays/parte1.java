package Aula12Arrays;

public class parte1 {
    public static void main(String[] args) {

        // Declaração de char com tamanho 5
        char vogais[] = new char[5];

        // a e i o u
        vogais[0] = 'a';
        vogais[1] = 'e';
        vogais[2] = 'i';
        vogais[3] = 'o';
        vogais[4] = 'u';

        // Imprimindo arrays separadamente
        System.out.println(vogais[0]);
        System.out.println(vogais[1]);
        System.out.println(vogais[2]);
        System.out.println(vogais[3]);
        System.out.println(vogais[4]);
    }
}
