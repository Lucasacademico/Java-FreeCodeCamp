package Aula06OperadoresLogicos;

public class parte6 {
    public static void main(String[] args) {

        int numero = 55; // A variável 'numero' é inicializada com o valor 55.
        System.out.println(++numero); // 'numero' é incrementado para 56 antes de ser impresso, portanto, 56 é
                                      // impresso.
        System.out.println(numero); // O valor atual de 'numero', que já é 56, é impresso novamente.
    }
}