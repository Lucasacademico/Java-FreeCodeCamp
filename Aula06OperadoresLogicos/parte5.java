package Aula06OperadoresLogicos;

public class parte5 {
    public static void main(String[] args) {

        int numero = 55; // A variável 'numero' é inicializada com o valor 55.
        System.out.println(numero++); // Primeiro, o valor atual de 'numero' (55) é impresso, depois 'numero' é...
                                      // ...incrementado para 56.
        System.out.println(numero); // O valor atualizado de 'numero' (56) é impresso.
    }
}
