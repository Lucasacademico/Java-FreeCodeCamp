package Aula13ForLoops;

public class parte6 {
    public static void main(String[] args) {

        // For realiza contagem de 1 Ã  50
        for (int i = 1; i <= 50; i++) {
            // if cria condicional para imprimir SOMENTE os valores impares
            if (i % 2 == 1) {
                System.out.println(i);
            }

        }
    }
}
