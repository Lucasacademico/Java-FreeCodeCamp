package Aula04Operadores;

public class parte1 {
    public static void main(String[] args) {
        int numero1 = 12;
        int numero2 = 6;

        // Adição
        System.out.println(numero1 + numero2);

        // Subtração
        System.out.println(numero1 - numero2);
        System.out.println(numero2 - numero1);

        // Multiplicação
        System.out.println(numero1 * numero2);

        // Divisão
        System.out.println(numero1 / numero2);

        // Resto da div
        System.out.println(numero1 % numero2);
    }
}
