        ArrayList<Integer> numero = new ArrayList<>();

        /*
         * As "Wrapper classes" (ou classes empacotadoras) no Java são uma parte
         * fundamental do wrapper ou embrulho dos tipos primitivos da linguagem. Java é
         * uma linguagem orientada a objetos, e em muitos contextos, como em coleções
         * genéricas (por exemplo, ArrayList, HashMap), é necessário usar objetos ao
         * invés de tipos primitivos. As Wrapper classes permitem que tipos primitivos
         * (como int, char, boolean) sejam usados em contextos que exigem objetos.
         * 
         * Cada tipo primitivo em Java tem uma classe Wrapper correspondente:
         * 
         * byte ➜ Byte
         * short ➜ Short
         * int ➜ Integer
         * long ➜ Long
         * float ➜ Float
         * double ➜ Double
         * char ➜ Character
         * boolean ➜ Boolean
         * Essas classes empacotadoras oferecem uma série de utilidades além de
         * simplesmente permitir o uso de tipos primitivos em contextos orientados a
         * objetos. Elas também contêm métodos úteis para converter entre tipos, parsear
         * strings, comparar valores, e mais. Por exemplo, a classe Integer oferece
         * métodos estáticos como parseInt(String), que converte uma string para um
         * inteiro, e toString(int), que converte um inteiro em uma string.
         */
